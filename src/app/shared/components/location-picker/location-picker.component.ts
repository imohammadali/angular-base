  showMarker() {
    if (this.location) {
      if (this.marker) {
        this.marker.setLngLat([this.location.long, this.location.lat]);
      } else {
        this.marker = new mapboxgl.Marker({ color: "#ff0000" }).setLngLat([this.location.long, this.location.lat]).addTo(this.map);
      }
    }
  }
